    //Adiciona um funcionario na tabela
    public void adicionaNaLista(Funcionario f) {
        String cargo = f.getClass().getName();
        cargo = cargo.substring(8); //Pra tirar o empresa.funcionarios.
        tableModel.addRow(new Object[] {f.getNome(), f.getDepartamento(), cargo } );
        System.out.println(f.getClass().getName());
    }

